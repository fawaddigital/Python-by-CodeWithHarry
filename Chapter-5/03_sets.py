# A set is a collection of unique items stored in a single variable.
'''
    Sets are written using curly braces {}.
    Sets do not allow duplicates.
    Sets are unordered (items have no fixed position).
    Sets are mutable (you can add or remove items).
'''
s = {1, 5, 32, 98, 5, 18, 5}
print(s)