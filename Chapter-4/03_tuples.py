''' Tuples are created using parentheses ().
They can store different types of data.
They are ordered (items keep their position).
They are immutable, meaning you cannot add, remove, or change items after creation. '''

a = () # empty tuple 
a = (1,) # tuple with only one element needs a comma 
a = (1,7,2) # tuple with more than one element 

data = (1, 3, 5, 18, False, "fawad", "ali")
print(data)

