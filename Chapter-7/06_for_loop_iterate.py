# for Loop Through a List
l = [1, 5, 9, 44, 60, 99]

# "Take each element from the list l one by one and store it in the variable i.
for i in l:
    print(i)

print("End of loop")


# for loop through a Tuple
t = {18, "fawad", False, "ali"}
for i in t:
    print(i)


